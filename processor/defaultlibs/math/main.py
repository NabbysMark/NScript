import math as _math

def sqrt(x):
    return _math.sqrt(x)

def pow(x, y):
    return _math.pow(x, y)

def abs(x):
    return _math.fabs(x)

def round(x, ndigits=0):
    return round(x, ndigits)

def sin(x):
    return _math.sin(x)

def cos(x):
    return _math.cos(x)

def tan(x):
    return _math.tan(x)

def log(x, base=_math.e):
    return _math.log(x, base)

def exp(x):
    return _math.exp(x)

def floor(x):
    return _math.floor(x)

def ceil(x):
    return _math.ceil(x)
